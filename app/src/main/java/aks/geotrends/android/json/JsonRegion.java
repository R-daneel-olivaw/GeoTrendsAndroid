package aks.geotrends.android.json;

public class JsonRegion {
	
	private String region;

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	@Override
	public String toString() {
		return "JsonRegion [region=" + region + "]";
	}

}
